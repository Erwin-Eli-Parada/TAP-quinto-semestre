    public void llenarTabla(){
        numero_clientes=0;
        String info="";
        String [] titulos={"CIF","nombre","direcci칩n","tfno1","tfno2"};
        m=new DefaultTableModel(null,titulos);
        jTable1.setModel(m);
        try{
            ResultSet r=sentencia.executeQuery("SELECT * FROM manempsa.Clientes;");
            while(r.next()){
                m=(DefaultTableModel) jTable1.getModel();
                String[] filaElemento={r.getString("CIF"),r.getString("nombre"),r.getString("direccion"),r.getString("tfno1"),r.getString("rfno2")};
                m.addRow(filaElemento);
                numero_clientes++;
            }
            jLabel17.setText(""+numero_clientes);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al crear consultar tabla");
        }
    }

