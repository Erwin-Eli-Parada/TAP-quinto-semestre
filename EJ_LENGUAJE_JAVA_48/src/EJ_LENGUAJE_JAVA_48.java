        try{
            ResultSet r=sentencia.executeQuery("SELECT CIF FROM manempsa.Clientes;");
            while(r.next()){
                if(consulta.equalsIgnoreCase(r.getString("CIF"))){
                    banner=true;
                }
            }
            if(banner==true){
                confirmacion=JOptionPane.showConfirmDialog(null,"Quiere borrar?","confirmaci칩n",JOptionPane.YES_NO_OPTION);
                if(confirmacion==JOptionPane.YES_OPTION){
                    sentencia.executeUpdate("DELETE FROM manempsa.Clientes WHERE CIF like '"+consulta+"';");
                    llenarTabla();
                }
            }else{
                JOptionPane.showMessageDialog(null,"No se ah encontrado");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al consultar tabla");
            System.out.println(e);
        }
