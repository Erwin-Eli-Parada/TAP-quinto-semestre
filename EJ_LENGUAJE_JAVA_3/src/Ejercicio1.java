    public boolean solonumeros(String s,int x){
        double a;
        try{
            a=Double.parseDouble(s); //aqui parseamos el string a doble si no es un doble saltara una excepcion y entra ala catch
            if(a<=x){ //para limitar que solo sean numeros validos mayores de x
                throw new Exception();
            }
        }catch(Exception exception){
            JOptionPane.showMessageDialog(null,"introduzca solo numeros mayores que "+x); //avisara que no introducio los datos correctos
            return false; //sale del metodo
        }
        return true; //retorna verdareo para poder seguir 
    }
